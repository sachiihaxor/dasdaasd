<div id="intro"><p align="justify">Association of Faculty of Economic and Business Indonesia (AFEBI) is a publisher of scientific papers from academicians to other professionals in the field of economics and business. It covers AFEBI Islamic Finance and Economic Review, AFEBI Accounting Review, AFEBI Economic and Finance Review, and AFEBI Management and Business Review. It is published twice a year in June and December. The objective of the journal is as the dissemination of theory and research in all areas of business and management, Islamic economics, accounting and economics.</p><p align="justify"><strong>Indexed:</strong></p><p align="justify"><a href="https://scholar.google.com/scholar?hl=id&amp;q=afebi&amp;as_sdt=0" target="_blank"><strong><img src="https://journal.uii.ac.id/public/site/images/deni/GS2.png" alt="" /></strong></a><strong><a href="https://garuda.kemdikbud.go.id/publisher/view/866" target="_blank"><img src="https://journal.uii.ac.id/public/site/images/deni/garuda.png" alt="" /></a><a href="https://search.crossref.org/?from_ui=&amp;q=AFEBI" target="_blank"><img src="https://journal.uii.ac.id/public/site/images/deni/crossref2.png" alt="" /></a><a href="https://app.dimensions.ai/discover/publication?search_mode=content&amp;search_text=AFEBI&amp;search_type=kws&amp;search_field=full_search&amp;and_facet_journal=jour.1151466" target="_blank"><img src="https://journal.uii.ac.id/public/site/images/deni/Dimension1.png" alt="" /></a></strong></p></div>
